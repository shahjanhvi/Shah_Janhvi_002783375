    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String name = txtName.getText();
        int empid = Integer.parseInt(txtEmpid.getText()) ;
        int age = Integer.parseInt(txtAge.getText()) ;
        //String gender = txtGender.getText();
       if(btnMale.isSelected()==true)
        {
            gender = "Male";
        }
        if(btnFemale.isSelected()==true)
        {
            gender = "Female";
        }
        if(btnOthers.isSelected()==true)
        {
            gender = "Others";
        }
        SimpleDateFormat sd=new SimpleDateFormat("MM-dd-yyyy");
        String stdate=sd.format(StDate.getDate());
        int Level = Integer.parseInt(txtLevel.getText()) ;
        String teaminfo = txtTeamInfo.getText() ;
        String position= txtPosition.getText() ;
        int phnnum = Integer.parseInt(txtPhnnum.getText()) ;
        String emailAdd= txtEmailAdd.getText();

        if(!(emailAdd.contains("@"))||!(emailAdd.endsWith(".com")))
        {
            JOptionPane.showMessageDialog(this, "Please enter correct details");
        }
        else
        {
            
        
        
        Employee em = history.addNewEmployee();
        em.setName(name);
        em.setGender(gender);
        em.setAge(age);
        em.setPhnnum(phnnum);
        em.setEmailadd(emailAdd);
        em.setPosition(position);
        em.setLevel(Level);
        em.setStdate(stdate);
        em.setTeamInfo(teaminfo);
        em.setEmpid(empid);
        
     
        JOptionPane.showMessageDialog(this ,"NEw EMPLOYEE ADDED");

        txtName.setText("");
        txtAge.setText("");
        btnGender.clearSelection();
        txtPhnnum.setText("");
        txtEmailAdd.setText("");
        txtPosition.setText("");
        txtLevel.setText("");
        StDate.setDate(null);
        txtEmpid.setText("");
        txtTeamInfo.setText("");
        lbPicture.setIcon(null);
        
        }
    
    }//GEN-LAST:event_btnSaveActionPerformed


    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        model.setRowCount(0);
        
        for(Community c: CommunityDirectory.getCommunityDirectory()){
            Object[] row = new Object[2];
            row[0] = c.getCommunity();
//            System.out.println(".............."+row[0]);
            row[1] = c.getCity().getCity();
            model.addRow(row);
            
        }
    }
    

    private void btnCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCityActionPerformed
        // TODO add your handling code here:
        if(txtCity.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please do not leave the City field empty!");
            return;
        } 
           String city = txtCity.getText();
            
           cityDirectory.addCity(new City(city));
           dropDownCity.addItem(city);
           JOptionPane.showMessageDialog(this, "New City Record Added.");
           txtCity.setText("");
    }//GEN-LAST:event_btnCityActionPerformed


    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCity.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to View!");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        Community c = (Community)model.getValueAt(selectedRowIndex, 0);
        
        CommunityDirectory.deleteCommunity(c);
        JOptionPane.showMessageDialog(this, "Community record deleted!");
        populateTable();
        txtViewCommunity.setText("");
        txtViewCity.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed



    private void btnCommunityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCommunityActionPerformed
        // TODO add your handling code here:
        if(txtCommunity.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please do not leave the Community field empty!");
            return;
        } 
        String city = dropDownCity.getSelectedItem().toString();
        String community = txtCommunity.getText();
        
        Community c = communityDirectory.addNewCommunity();
        c.setCommunity(community);
        c.setCity(new City(city));
        JOptionPane.showMessageDialog(this, "New Community Record Added.");
        populateTable();
        txtCommunity.setText("");
    }//GEN-LAST:event_btnCommunityActionPerformed


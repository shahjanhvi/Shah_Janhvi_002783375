    private void populateTable() {
        DefaultTableModel model=  (DefaultTableModel)tblEmployeeHistory.getModel();
        model.setRowCount(0);
        
        for(Employee em:history.getHistory()){
            Object[] row  =new Object[10];
            row[0]=em;
            row[1]=em.getAge();
            row[2]=em.getGender();
            row[3]=em.getPhnnum();
            row[4]=em.getEmailadd();
            row[5]=em.getEmpid();
            row[6]=em.getPosition();
            row[7]=em.getLevel();
            row[8]=em.getStdate();
            row[9]=em.getTeamInfo();
            
            model.addRow(row);
            
            
            
            
        }
    }

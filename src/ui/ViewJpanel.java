    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
      int i=tblEmployeeHistory.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel) tblEmployeeHistory.getModel();
        if(i>=0)
        {
            model.setValueAt(txtName.getText(), i, 0);
            model.setValueAt(txtAge.getText(), i, 1);
            model.setValueAt(txtGender.getText(), i, 2);
            model.setValueAt(txtPhnnum.getText(), i, 3);
            model.setValueAt(txtEmailAdd.getText(), i, 4);
            model.setValueAt(txtEmpId.getText(), i, 5);
            model.setValueAt(txtPosition.getText(), i, 6);
            model.setValueAt(txtLevel.getText(), i, 7);
            model.setValueAt(txtTeamInfo.getText(), i, 8);
            model.setValueAt(txtStDate.getText(), i, 9);
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex=tblEmployeeHistory.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel model=(DefaultTableModel) tblEmployeeHistory.getModel();
        Employee selectednewEmployee=(Employee)model.getValueAt(selectedRowIndex,0);
        history.deletenewEmployee(selectednewEmployee);
        
        
       JOptionPane.showMessageDialog(this, "Employee deleted");
       populateTable();
        
        
         
        
    }//GEN-LAST:event_btnDeleteActionPerformed


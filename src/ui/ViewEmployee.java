    private void Table() {
        
        DefaultTableModel model =(DefaultTableModel) tblEmployeeHistory.getModel();
        model.setRowCount(0);
        
        for(Employee em: EmployeeHistory.getHistory()){
            Object row[]=new Object[10];
            row[0] = em.getEmpId();
            row[1] = em.getName();
            row[2] = em.getAge();
            row[3] = em.getGender();
            row[4] = em.getStDate();
            row[5] = em.getLevel();
            row[6] = em.getTeamInfo();
            row[7] = em.getPosition();
            row[8] = em.getPhnNum();
            row[9] = em.getEmailAdd();
            
            model.addRow(row);
            
        }
    }
